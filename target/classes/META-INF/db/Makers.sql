CREATE TABLE PRCOMMENT (
        PRCNUM NUMBER(10) PRIMARY KEY,
        MID VARCHAR2(40) REFERENCES MEMBER(MID) NOT NULL,
        PNUM NUMBER(10) REFERENCES PRODUCT(PNUM) NOT NULL,
